    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
        
        if ((jTCpf.getText().length() > 0)){
            
            PrefeituraServiceIT ps = Conexoes.prefeituraService();
            Funcionario f = ps.pesquisarFuncionario(prefeitura, jTCpf.getText());
            
            if (f != null){
                jPMsgNaoVinculado.setVisible(false);
                jPResultadoPesquisa.setVisible(true);
                
                jTNome.setText(f.getNome());
                jTEmail.setText(f.getEmail());
                
                jTNome.setEditable(false);
                jTEmail.setEditable(false);
                
            } else {
                jPMsgNaoVinculado.setVisible(true);
                jPResultadoPesquisa.setVisible(false);
            }
            
        }else {
            
        }
        
        
        revalidate();
        repaint();
        
    }//GEN-LAST:event_jButton1ActionPerformed

