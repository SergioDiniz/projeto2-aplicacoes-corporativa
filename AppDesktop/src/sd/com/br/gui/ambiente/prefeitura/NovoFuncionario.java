    private void jBBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBBuscarActionPerformed
        // TODO add your handling code here:
        
        
        
        if(jTCPF.getText().length() > 0){
            FuncionarioServiceIT fs = Conexoes.funcionarioService();
            
            Funcionario f = fs.buscarPorCPF(jTCPF.getText());
            
            if(f != null){
                jPDadosCompletos.setVisible(true);
                jLMsgNaoCadastrado.setVisible(false);
                jTSenha.setVisible(false);
                jLSenha.setVisible(false);
                
                jTNome.setText(f.getNome());
                jTEmail.setText(f.getEmail());
                
                jTNome.setEditable(false);
                jTEmail.setEditable(false);
                
                jBVincular.setVisible(true);
                jBCadastrar.setVisible(false);
                
                
            } else {
                jPDadosCompletos.setVisible(true);
                jLMsgNaoCadastrado.setVisible(true);
                jTSenha.setVisible(true);
                jLSenha.setVisible(true);
                
                jTNome.setText("");
                jTEmail.setText("");
                
                jTNome.setEditable(true);
                jTEmail.setEditable(true);
                
                jBCadastrar.setVisible(true);
                jBVincular.setVisible(false);
                
            }
            
            
        }else{
            JOptionPane.showMessageDialog(jPanel1, "Preencha todos os campos corretamente!");
        }
        
        revalidate();
        repaint();
        
        
    }//GEN-LAST:event_jBBuscarActionPerformed

    private void jBCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBCadastrarActionPerformed
        // TODO add your handling code here:
        
        
        if((jTCPF.getText().length() > 0) && (jTNome.getText().length() > 0) && (jTEmail.getText().length() > 0) &&
                (jTSenha.getText().length() > 0)){
            
            DAOIT dao = Conexoes.DAO();
            PrefeituraServiceIT ps = Conexoes.prefeituraService();
            Funcionario f = new Funcionario(jTNome.getText(), jTCPF.getText(), jTEmail.getText(), jTSenha.getText());
            
            dao.salvar(f);
            
            String resultado = ps.cadastrarNaPrefeitura(prefeitura, f);
            JOptionPane.showMessageDialog(jPanel1, resultado);
            
            
        } else {
            JOptionPane.showMessageDialog(jPanel1, "Preencha todos os campos corretamente!");
        }
        
    }//GEN-LAST:event_jBCadastrarActionPerformed

    private void jBVincularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBVincularActionPerformed
        // TODO add your handling code here:
         
            if(jTCPF.getText().length() > 0){
                
                FuncionarioServiceIT fs = Conexoes.funcionarioService();
                PrefeituraServiceIT ps = Conexoes.prefeituraService();
                
                Funcionario f = fs.buscarPorCPF(jTCPF.getText());
                
                String resultado = ps.vincular(prefeitura, f);
                JOptionPane.showMessageDialog(jPanel1, resultado);   
                
            } else {
                JOptionPane.showMessageDialog(jPanel1, "Preencha todos os campos corretamente!");   
            }
            
            
        
        
        
    }//GEN-LAST:event_jBVincularActionPerformed


