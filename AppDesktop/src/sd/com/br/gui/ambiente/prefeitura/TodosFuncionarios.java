    public void iniciarTabela(){
        
        
        
        if(prefeitura.getFuncionarios() != null && prefeitura.getFuncionarios().size() > 0){
            ArrayList dados = new ArrayList();
            String[] colunas = new String[]{"Nome", "CPF", "Email"};
            
            for(int i=0; i < prefeitura.getFuncionarios().size();i++){
                dados.add(new Object[]{ prefeitura.getFuncionarios().get(i).getNome(),
                                        prefeitura.getFuncionarios().get(i).getCpf(),
                                        prefeitura.getFuncionarios().get(i).getEmail()
                                      });
            }
            
            ModeloTabela tabela = new ModeloTabela(dados, colunas);
            jTFuncionarios.setModel(tabela);
            jTFuncionarios.setRowHeight(40);
            jTFuncionarios.getTableHeader().setReorderingAllowed(false);
            
            
        }else{
            JOptionPane.showMessageDialog(jPanel1, "NÃ£o foi Encontrado nenhum resultado!");
            jPResultadoFuncionario.setVisible(false);
        }
        
        revalidate();
        repaint();
        
    }
    

